package project660;

public class Pair 
{
	private int n;
	private double p;
	
	public Pair() {
		
	}
	
	public Pair(int n2, double p2) {
		setN(n2);
		setP(p2);
	}

	public int getN() {
		return n;
	}

	public void setN(int n) {
		this.n = n;
	}

	public double getP() {
		return p;
	}

	public void setP(double p) {
		this.p = p;
	}
	
}
